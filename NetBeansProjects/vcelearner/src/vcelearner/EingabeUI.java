    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!neueLK) {
            s.removeLernKarte();
            fillFields();
        } else {
            txtAreaFrage.setText("");
            txtFieldID.setText(null);
            txtFieldSG.setText(null);
            for (int i = 0; i < 8; i++) {
                txtAreasAntwort[i].setText("");
            }
            for (int i = 0; i < 8; i++) {
                checkBoxesAntwort[i].setSelected(false);
            }
            for (int i = 0; i < 7; i++) {
                checkBoxesThema[i].setSelected(false);
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        LernKarte lK = new LernKarte(txtAreaFrage.getText(), Integer.parseInt(txtFieldSG.getText()));
        ArrayList<PotentielleAntwort> pAs = new ArrayList<>();
        for (int i = 0; i < txtAreasAntwort.length; i++) {
            if (!txtAreasAntwort[i].getText().equals("")) {
                PotentielleAntwort pA = new PotentielleAntwort(checkBoxesAntwort[i].isSelected(), txtAreasAntwort[i].getText());
                pAs.add(pA);
            } else {
                break;
            }
        }
        lK.setpAs(pAs);

        ArrayList<ThemenBereich> tBs = new ArrayList<>();
        for (int i = 0; i < checkBoxesThema.length; i++) {
            if (checkBoxesThema[i].isSelected()) {
                ThemenBereich tb = new ThemenBereich(i + 1, checkBoxesThema[i].getText());
                tBs.add(tb);
            }
        }
        lK.settBs(tBs);

        if (!neueLK) {
            lK.setId(s.getAktuelleLernKarte().getId());
            s.setAktuelleLernKarte(lK);
        } else {
            s.addLernKarte(lK);
            neueLK = false;
            btnAbbruch.setVisible(false);
            btnGoTo.setVisible(true);
            btnNext.setVisible(true);
            btnPrev.setVisible(true);
            txtFieldGoToID.setVisible(true);

        }

    }//GEN-LAST:event_btnSaveActionPerformed

