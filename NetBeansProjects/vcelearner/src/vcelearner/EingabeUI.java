    public void fillFields() {
        txtAreaFrage.setText(s.getAktuelleLernKarte().getFrage());
        txtFieldID.setText(String.valueOf(s.getAktuelleLernKarte().getId()));
        txtFieldSG.setText(String.valueOf(s.getAktuelleLernKarte().getSchwierigkeitsGrad()));
        for (int i = 0; i < 8; i++) {
            if (i < s.getAktuelleLernKarte().getpAs().size()) {
                txtAreasAntwort[i].setText(s.getAktuelleLernKarte().getpAs().get(i).getAntwort());
            } else {
                txtAreasAntwort[i].setText("");
            }
        }
        for (int i = 0; i < 8; i++) {
            if (i < s.getAktuelleLernKarte().getpAs().size()) {
                checkBoxesAntwort[i].setSelected(s.getAktuelleLernKarte().getpAs().get(i).isRichtigkeit());
            } else {
                checkBoxesAntwort[i].setSelected(false);
            }
        }
        for (int i = 0; i < 7; i++) {
            checkBoxesThema[i].setSelected(false);
        }
        for (ThemenBereich tB : s.getAktuelleLernKarte().gettBs()) {
            checkBoxesThema[tB.getId() - 1].setSelected(true);
        }
    }

